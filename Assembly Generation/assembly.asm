	MOV i2, AX
L4:
	MOV AX, i2
	CMP AX, 4
	JL L0
	MOV t0, 0
	JMP L1
L0:
	MOV t0, 1
L1:
	MOV AX, t0
	CMP AX, 0
	JE L5
	MOV AX, i2
	ADD AX, 1
	MOV i2, AX
	MOV AX, 3
	MOV a3, AX
