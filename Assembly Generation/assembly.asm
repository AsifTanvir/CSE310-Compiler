OUTDEC PROC
	PUSH AX
	PUSH BX
	PUSH CX
	PUSH DX

	OR AX,AX
	JGE NEXT

	PUSH AX
	MOV AH,2
	MOV DL,'-'
	INT 21h
	POP AX
	NEG AX

NEXT:
	XOR CX,CX
	MOV BX,10D

ASIF:
	XOR DX,DX
	DIV BX
	PUSH DX
	INC CX
	OR AX,AX
	JNE ASIF

	MOV AH,2
PRINT_LOOP:
